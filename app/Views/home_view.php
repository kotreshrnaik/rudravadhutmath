                        <P> ಇವರು  ಬೆಳಗಾವಿ ಜಿಲ್ಲೆಯ ಅಥಣಿ ತಾಲೂಕಿನ ಜಿಂಜರವಾಡ ಎಂಬ ಗ್ರಾಮದಲ್ಲಿ 1939 ರಲ್ಲಿ ಜನಿಸಿದರು.
 ನಂತರ ನಾಲ್ಕನೇ ವಯಸ್ಸಿನಲ್ಲಿ ತಂದೆಯವರು ತೀರಿಕೊಂಡರು. ಆಗ ತಾಯಿಯವರು ತವರುಮನೆಯಾದ ವಿಜಯಪೂರ ಜಿಲ್ಲೆಯ ಬಸವನ ಬಾಗೆವಾಡಿ ತಾಲೂಕಿನ , ಮನಗುಳಿ ಎಂಬ ಗ್ರಾಮದಲ್ಲಿ ಬಂದು ವಾಸಿಸಿದರು. ಮುಂದೆ ಎರಡು ವರ್ಷದಲ್ಲಿ ಅಂದರೆ ಇವರ ಆರನೇ ವಯಸ್ಸಿಗೆ ತಾಯಿಯವರೂ ತೀರಿಕೊಂಡರು . ಆಮೇಲೆ ಇವರು ಸ್ವತಃ ತಾವೊಬ್ಬರೆ ಶಾಲೆಗೆ ಹೋಗಿ ಹೆಸರು ಹಚ್ಚಿದರು.</P>
 <P>ಪರಸ್ಥಿತಿಗೆ ಅನುಗುಣವಾಗಿ ಸ್ವತಃ ಯಾವುದೆ ಪುಸ್ತಕಗಳನ್ನು ಕೊಂಡುಕೊಳ್ಳಲಾಗದೆ , 1ರಿಂದ 7ನೇ ತರಗತಿವರೆಗೆ ಗೆಳೆಯರಿಂದ ಸ್ವಲ್ಪ ಸಮಯದವರೆಗೆ ಪುಸ್ತಕಗಳನ್ನು ತೆಗೆದುಕೊಂಡು, ಓದಿ ಮರಳಿ ಕೊಡುತ್ತಿದ್ದರು. ವಿಶೇಷವೆಂದರೆ ಆ ಶಾಲೆಯಲ್ಲಿ ಮುಂಚೂಣಿಯಲ್ಲಿದ್ದರು . ಮುಂದೆ ಇದನ್ನು ಸಹಿಸದೆ ಗೆಳೆಯರು ಪುಸ್ತಕ ಕೊಡದೆ ಮನಬಂದತೆ ಬೈದರು. ಆಗ ಇವರು ಮನನೋಂದುಕೊಂಡು ಶಾಲೆ ಬಿಟ್ಟು ಯಾರದೋ ಸಹಾಯದಿಂದ ಮುಂಬಯಿಗೆ ಹೋದರು. ಅಲ್ಲಿ ಉಪಜೀವನಕ್ಕಾಗಿ ಕೆಲಸಮಾಡಲಾರಂಬಿಸಿದರು. 
 <P>ಕೆಲವು ವರ್ಷಗಳ ನಂತರ ಪ್ರಿಮಿಯರ ಕಂಪನಿಯಲ್ಲಿ ಡ್ರೈವರಾಗಿ ನೌಕರಿ ಹಿಡಿದು, ಗ್ರಹಸ್ಥ ಜೀವನವನ್ನು ನಡೆಸುತ್ತಿರುವಾಗ , ಅಧ್ಯಾತ್ಮದಕಡೆ ಆಸಕ್ತಿಹುಟ್ಟಿತು. ಆಗ ಹುಬ್ಬಳ್ಳಿಗೆ ಬಂದು ಶ್ರೀ ಜಡಿಮಠದ ಶ್ರೀ ಶಿವಾನಂದ ಪರಮಹಂಸರಿಂದ ಗುರು ಉಪದೇಶ ಪಡೆದರು. ಕೆಲವು ವರ್ಷಗಳ ನಂತರ ಶಾಸ್ತ್ರ ಅಭ್ಯಾಸಕ್ಕಾಗಿ ನೌಕರಿಗೆ ರಾಜಿನಾಮೆ ಕೊಟ್ಟು,  ಹುಬ್ಬಳ್ಳಿಯಲ್ಲಿ ಶ್ರೀ ಸಿಧ್ಧಾರೂಢರ ಶಿಷ್ಯರಾದ ಆರೂಢಜ್ಯೋತಿ ಶ್ರೀ ಶಿವಪುತ್ರ ಮಹಾಸ್ವಾಮಿಗಳಿಂದ ನಿಜಗುಣರ ಶಾಸ್ತ್ರವನ್ನು ,  ಸೋಲಾಪುರದಲ್ಲಿ ಶ್ರೀ ವಿದ್ಯಾನಂದ ಸ್ವಾಮಿಗಳಿಂದ ವಿಚಾರ ಸಾಗರ , ವೃತ್ತಿಪ್ರಭಾಕರಾದಿ ಹಿಂದಿ ಶಾಸ್ತ್ರಗಳನ್ನು ಅಭ್ಯಾಸಮಾಡಿದರು.        ಆಗ ಅವರ ವಿಧ್ಯೆಯನ್ನು ನೋಡಿ ಅನೇಕ ಭಕ್ತರು ಸೇರಿ ಜಮಖಂಡಿಯ ಶ್ರೀ ರುದ್ರಾವಧೂತ ಮಠವನ್ನು ಮೊದಲಮಾಡಿಕೊಂಡು ಅನೇಕ ಮಠಗಳನ್ನು ಇವರಿಗೆ ಒಪ್ಪಿಸಿದರು .  ಮರಳಿಬಂದು ಬಟ್ಟೆಯ ವ್ಯಾಪಾರವನ್ನು ಮಾಡುತ್ತ ಕೋಟ್ಯಾಧಿಶರಾದರು.</P>  
 <P>ಇವರು 60 ನೇ ವಯಸ್ಸಿನ ನಂತರ ಸಂಪೂರ್ಣಾನಂದ ಸಂಸ್ಕೃತ ವಿಶ್ವವಿದ್ಯಾಲಯ ವಾರಾಣಾಸಿ ದಿಂದ ವೇದಾಂತಾಚಾರ್ಯ(ಸಂಸ್ಕೃತ ಎಂ, ಎ ) ಪದವಿಯನ್ನು ಪಡೆದರು.  ಮಠಗಳ ಬೆಳವಣಿಗೆಯ ಉದ್ಧೇಶದಿಂದ ಗ್ರಹಸ್ಥಾಶ್ರಮವನ್ನು ತ್ಯಾಗಮಾಡಿ, ಮುಂಬಯಿ ಕಾಂದಿವಲಿಯ ಆನಂದವನ ಆಶ್ರಮದ ಮಹಾಮಂಡಲೇಶ್ವರರಾದ, ದ್ವಾದಶದರ್ಶನ ಕಾನನ ಕೇಸರಿ ಎಂದು ಬಿರುದಾಂಕಿತರಾದ , ಸ್ವಾಮಿ ಶ್ರೀ ಕಾಶಿಕಾನಂದ ಮಹಾರಾಜರಿಂದ ನೈಷ್ಟಕ ಬ್ರಹ್ಮಚಾರಿ ದಿಕ್ಷೆ ಪಡೆದು, ವೇದ ಉಪನಿಷತ್ತುಗಳ ಅಭ್ಯಾಸವನ್ನು ಮಾಡಿದರು . ಕೆಲವು ವರ್ಷದ ನಂತರ ಮುಂಬಯಿಯ ವಿಲೇಪಾರ್ಲೆಯ ಸಂನ್ಯಾಸ ಆಶ್ರಮದ ಪೀಠಾಧೀಶರಾದ ಮಹಾಮಂಡಲೆಶ್ವರ ಸ್ವಾಮಿ ಶ್ರೀ ವಿಶ್ವೇಶ್ವರಾನಂದ ಗಿರಿಯವರಿಂದ 1998 ರಲ್ಲಿ  ಸಂನ್ಯಾಸ ದೀಕ್ಷಯನ್ನು ಪಡೆದರು .
